# Basic Math operations

max(5, 10, 15)   # 15
min(5, 10.5, 15) # 5
sqrt(100)        # 10

abs(-4.7)        # 4.7  
ceiling(1.4)     # 2  
floor(1.4)       # 1

# --------------------------------------------------------------------- #
# Note that ceiling and floor convert to Integers type (whole numbers with no decimal)!

p <- 10.01
p # 10.01
ceiling(p) # 11
floor(p)   # 10

